package ia.entity;

import java.math.BigDecimal;
import java.sql.Timestamp;
import org.seasar.doma.Column;
import org.seasar.doma.Entity;
import org.seasar.doma.Id;
import org.seasar.doma.Table;
import org.seasar.doma.Version;

/**
 * 
 */
@Entity(listener = TItemListener.class)
@Table(name = "T_ITEM")
public class TItem {

    /**  */
    @Id
    @Column(name = "ITEM_ID")
    String itemId;

    /**  */
    @Column(name = "ITEM_NAME")
    String itemName;

    /**  */
    @Column(name = "CLASS_ID")
    String classId;

    /**  */
    @Column(name = "BLAND")
    String bland;

    /**  */
    @Column(name = "ITEM_COMMENT")
    String itemComment;

    /**  */
    @Column(name = "ITEM_PRICE")
    BigDecimal itemPrice;

    /**  */
    @Column(name = "ITEM_COST")
    BigDecimal itemCost;

    /**  */
    @Column(name = "ITEM_PROFIT")
    BigDecimal itemProfit;

    /**  */
    @Column(name = "ITEM_COST_JPY")
    BigDecimal itemCostJpy;

    /**  */
    @Column(name = "BAR_CODE")
    String barCode;

    /**  */
    @Column(name = "SIZE")
    String size;

    /**  */
    @Column(name = "WIGHT")
    BigDecimal wight;

    /**  */
    @Column(name = "PIC_URL")
    String picUrl;

    /**  */
    @Column(name = "COMMENT")
    String comment;

    /**  */
    @Column(name = "REG_USER_ID")
    String regUserId;

    /**  */
    @Column(name = "REG_TIME")
    Timestamp regTime;

    /**  */
    @Column(name = "UPD_USER_ID")
    String updUserId;

    /**  */
    @Column(name = "UPD_TIME")
    Timestamp updTime;

    /**  */
    @Version
    @Column(name = "VERSION_NO")
    Integer versionNo;

    /** 
     * Returns the itemId.
     * 
     * @return the itemId
     */
    public String getItemId() {
        return itemId;
    }

    /** 
     * Sets the itemId.
     * 
     * @param itemId the itemId
     */
    public void setItemId(String itemId) {
        this.itemId = itemId;
    }

    /** 
     * Returns the itemName.
     * 
     * @return the itemName
     */
    public String getItemName() {
        return itemName;
    }

    /** 
     * Sets the itemName.
     * 
     * @param itemName the itemName
     */
    public void setItemName(String itemName) {
        this.itemName = itemName;
    }

    /** 
     * Returns the classId.
     * 
     * @return the classId
     */
    public String getClassId() {
        return classId;
    }

    /** 
     * Sets the classId.
     * 
     * @param classId the classId
     */
    public void setClassId(String classId) {
        this.classId = classId;
    }

    /** 
     * Returns the bland.
     * 
     * @return the bland
     */
    public String getBland() {
        return bland;
    }

    /** 
     * Sets the bland.
     * 
     * @param bland the bland
     */
    public void setBland(String bland) {
        this.bland = bland;
    }

    /** 
     * Returns the itemComment.
     * 
     * @return the itemComment
     */
    public String getItemComment() {
        return itemComment;
    }

    /** 
     * Sets the itemComment.
     * 
     * @param itemComment the itemComment
     */
    public void setItemComment(String itemComment) {
        this.itemComment = itemComment;
    }

    /** 
     * Returns the itemPrice.
     * 
     * @return the itemPrice
     */
    public BigDecimal getItemPrice() {
        return itemPrice;
    }

    /** 
     * Sets the itemPrice.
     * 
     * @param itemPrice the itemPrice
     */
    public void setItemPrice(BigDecimal itemPrice) {
        this.itemPrice = itemPrice;
    }

    /** 
     * Returns the itemCost.
     * 
     * @return the itemCost
     */
    public BigDecimal getItemCost() {
        return itemCost;
    }

    /** 
     * Sets the itemCost.
     * 
     * @param itemCost the itemCost
     */
    public void setItemCost(BigDecimal itemCost) {
        this.itemCost = itemCost;
    }

    /** 
     * Returns the itemProfit.
     * 
     * @return the itemProfit
     */
    public BigDecimal getItemProfit() {
        return itemProfit;
    }

    /** 
     * Sets the itemProfit.
     * 
     * @param itemProfit the itemProfit
     */
    public void setItemProfit(BigDecimal itemProfit) {
        this.itemProfit = itemProfit;
    }

    /** 
     * Returns the itemCostJpy.
     * 
     * @return the itemCostJpy
     */
    public BigDecimal getItemCostJpy() {
        return itemCostJpy;
    }

    /** 
     * Sets the itemCostJpy.
     * 
     * @param itemCostJpy the itemCostJpy
     */
    public void setItemCostJpy(BigDecimal itemCostJpy) {
        this.itemCostJpy = itemCostJpy;
    }

    /** 
     * Returns the barCode.
     * 
     * @return the barCode
     */
    public String getBarCode() {
        return barCode;
    }

    /** 
     * Sets the barCode.
     * 
     * @param barCode the barCode
     */
    public void setBarCode(String barCode) {
        this.barCode = barCode;
    }

    /** 
     * Returns the size.
     * 
     * @return the size
     */
    public String getSize() {
        return size;
    }

    /** 
     * Sets the size.
     * 
     * @param size the size
     */
    public void setSize(String size) {
        this.size = size;
    }

    /** 
     * Returns the wight.
     * 
     * @return the wight
     */
    public BigDecimal getWight() {
        return wight;
    }

    /** 
     * Sets the wight.
     * 
     * @param wight the wight
     */
    public void setWight(BigDecimal wight) {
        this.wight = wight;
    }

    /** 
     * Returns the picUrl.
     * 
     * @return the picUrl
     */
    public String getPicUrl() {
        return picUrl;
    }

    /** 
     * Sets the picUrl.
     * 
     * @param picUrl the picUrl
     */
    public void setPicUrl(String picUrl) {
        this.picUrl = picUrl;
    }

    /** 
     * Returns the comment.
     * 
     * @return the comment
     */
    public String getComment() {
        return comment;
    }

    /** 
     * Sets the comment.
     * 
     * @param comment the comment
     */
    public void setComment(String comment) {
        this.comment = comment;
    }

    /** 
     * Returns the regUserId.
     * 
     * @return the regUserId
     */
    public String getRegUserId() {
        return regUserId;
    }

    /** 
     * Sets the regUserId.
     * 
     * @param regUserId the regUserId
     */
    public void setRegUserId(String regUserId) {
        this.regUserId = regUserId;
    }

    /** 
     * Returns the regTime.
     * 
     * @return the regTime
     */
    public Timestamp getRegTime() {
        return regTime;
    }

    /** 
     * Sets the regTime.
     * 
     * @param regTime the regTime
     */
    public void setRegTime(Timestamp regTime) {
        this.regTime = regTime;
    }

    /** 
     * Returns the updUserId.
     * 
     * @return the updUserId
     */
    public String getUpdUserId() {
        return updUserId;
    }

    /** 
     * Sets the updUserId.
     * 
     * @param updUserId the updUserId
     */
    public void setUpdUserId(String updUserId) {
        this.updUserId = updUserId;
    }

    /** 
     * Returns the updTime.
     * 
     * @return the updTime
     */
    public Timestamp getUpdTime() {
        return updTime;
    }

    /** 
     * Sets the updTime.
     * 
     * @param updTime the updTime
     */
    public void setUpdTime(Timestamp updTime) {
        this.updTime = updTime;
    }

    /** 
     * Returns the versionNo.
     * 
     * @return the versionNo
     */
    public Integer getVersionNo() {
        return versionNo;
    }

    /** 
     * Sets the versionNo.
     * 
     * @param versionNo the versionNo
     */
    public void setVersionNo(Integer versionNo) {
        this.versionNo = versionNo;
    }
}